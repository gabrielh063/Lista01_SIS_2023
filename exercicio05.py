# escrever um algoritmo que receba dois numeros e ao final mostre a soma subtraçao multiplicaçao e a divisao dos numeros lidos
A = float(input('Digite o primeiro valor: '))
B = float(input('Digite o segundo valor: '))
C = A + B
D = A - B
E = A * B
F = A / B
print(f'O Resultado da soma é: {C}')
print(f'O Resultado da subtração é: {D}')
print(f'O Resultado da multiplicação é: {E}')
print(f'O Resultado da divisão é: {F}')